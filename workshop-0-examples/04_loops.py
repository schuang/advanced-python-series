# For loop: iterates over a sequence
print("For loop (list):")
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# While loop: repeats as long as a condition is true
print("\nWhile loop:")
count = 0
while count < 3:
    print(f"Count is {count}")
    count += 1


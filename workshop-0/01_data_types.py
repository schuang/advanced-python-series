# 01_data_types.py

# Integers are whole numbers
a = 5
print(f"Integer: {a}")

# Floats are numbers with a decimal point
b = 3.14
print(f"Float: {b}")

# Strings are sequences of characters
c = "Hello, World!"
print(f"String: {c}")

# Booleans are either True or False
d = True
print(f"Boolean: {d}")
